public class Point {
    private double x, y, z;
    public Point(double  aa, double bb, double cc){
        this.x=aa;
        this.y=bb;
        this.z=cc;
    }
    public double getA(){
        return x;
    }
    public double getB(){
        return y;
    }
    public double getC(){
        return z;
    }
}
